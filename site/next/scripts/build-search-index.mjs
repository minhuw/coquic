import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const nextRoot = path.resolve(scriptDir, '..');
process.chdir(nextRoot);

const docs = [
  { href: '/docs', file: 'README.md', section: 'Start', label: 'Overview' },
  { href: '/docs/api/public-api', file: 'api/public-api.md', section: 'API Surface', label: 'Public API' },
  { href: '/docs/api/core', file: 'api/core.md', section: 'API Surface', label: 'Core API' },
  { href: '/docs/api/quic', file: 'api/quic.md', section: 'API Surface', label: 'QUIC Facade API' },
  { href: '/docs/api/http3', file: 'api/http3.md', section: 'API Surface', label: 'HTTP/3 API' },
  { href: '/docs/api/c-ffi', file: 'api/c-ffi.md', section: 'Native Bindings', label: 'C FFI API' },
  { href: '/docs/api/c-ffi-reference', file: 'api/c-ffi-reference.md', section: 'Native Bindings', label: 'C FFI Reference' },
  { href: '/docs/api/rust-wrapper', file: 'api/rust-wrapper.md', section: 'Native Bindings', label: 'Rust Wrappers' },
  { href: '/docs/api/integration', file: 'api/integration.md', section: 'Runtime', label: 'Runtime Integration' },
];

const records = docs.map((doc) => {
  const markdown = readDocMarkdown(doc.file);
  const title = extractTitle(markdown) || doc.label;
  const headings = extractHeadings(markdown).filter((heading) => heading !== title);
  const body = markdownToPlainText(markdown);
  return {
    id: `docs-${doc.href === '/docs' ? 'overview' : doc.href.replace(/^\/docs\//, '').replace(/\//g, '-')}`,
    title,
    href: doc.href,
    section: doc.section,
    description: extractDescription(body),
    kind: 'docs',
    headings,
    keywords: keywordCandidates(markdown),
    body,
  };
});

mkdirSync('src/generated', { recursive: true });
writeFileSync(
  'src/generated/site-search-docs.ts',
  `// Generated by scripts/build-search-index.mjs. Do not edit by hand.\n\n` +
    `import type { SiteSearchItem } from '@/lib/search-index';\n\n` +
    `export const generatedDocSearchItems = ${JSON.stringify(records, null, 2)} as const satisfies readonly SiteSearchItem[];\n`,
);

function readDocMarkdown(file) {
  switch (file) {
    case 'README.md':
      return existsSync('../../docs/README.md') ? readFileSync('../../docs/README.md', 'utf-8') : '';
    case 'api/public-api.md':
      return existsSync('../../docs/api/public-api.md') ? readFileSync('../../docs/api/public-api.md', 'utf-8') : '';
    case 'api/core.md':
      return existsSync('../../docs/api/core.md') ? readFileSync('../../docs/api/core.md', 'utf-8') : '';
    case 'api/quic.md':
      return existsSync('../../docs/api/quic.md') ? readFileSync('../../docs/api/quic.md', 'utf-8') : '';
    case 'api/http3.md':
      return existsSync('../../docs/api/http3.md') ? readFileSync('../../docs/api/http3.md', 'utf-8') : '';
    case 'api/c-ffi.md':
      return existsSync('../../docs/api/c-ffi.md') ? readFileSync('../../docs/api/c-ffi.md', 'utf-8') : '';
    case 'api/c-ffi-reference.md':
      return existsSync('../../docs/api/c-ffi-reference.md')
        ? readFileSync('../../docs/api/c-ffi-reference.md', 'utf-8')
        : '';
    case 'api/rust-wrapper.md':
      return existsSync('../../docs/api/rust-wrapper.md') ? readFileSync('../../docs/api/rust-wrapper.md', 'utf-8') : '';
    case 'api/integration.md':
      return existsSync('../../docs/api/integration.md') ? readFileSync('../../docs/api/integration.md', 'utf-8') : '';
    default:
      throw new Error(`unhandled documentation page: ${file}`);
  }
}

function extractTitle(markdown) {
  return markdown.match(/^#\s+(.+)$/m)?.[1]?.trim() || '';
}

function extractHeadings(markdown) {
  return [...markdown.matchAll(/^#{1,3}\s+(.+)$/gm)].map((match) => cleanInlineMarkdown(match[1]));
}

function extractDescription(text) {
  const compact = text.replace(/\s+/g, ' ').trim();
  if (compact.length <= 180) return compact || 'CoQUIC documentation page.';
  return `${compact.slice(0, 177).trimEnd()}...`;
}

function keywordCandidates(markdown) {
  const codeTerms = [...markdown.matchAll(/`([^`]{2,80})`/g)].map((match) => match[1].trim());
  return [...new Set(codeTerms)]
    .filter((term) => /^[A-Za-z0-9_.:/-]+$/.test(term))
    .slice(0, 48);
}

function markdownToPlainText(markdown) {
  return cleanInlineMarkdown(
    markdown
      .replace(/\r\n/g, '\n')
      .replace(/```[\s\S]*?```/g, (block) => block.replace(/```[A-Za-z0-9_+-]*\n?|```/g, ' '))
      .replace(/^#{1,6}\s+/gm, '')
      .replace(/^\s*[-*+]\s+/gm, '')
      .replace(/^\s*\d+\.\s+/gm, '')
      .replace(/^\|(.+)\|$/gm, '$1')
      .replace(/\|/g, ' ')
      .replace(/\n{2,}/g, '\n'),
  );
}

function cleanInlineMarkdown(text) {
  return text
    .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/[*_~]/g, '')
    .replace(/[<>]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}
